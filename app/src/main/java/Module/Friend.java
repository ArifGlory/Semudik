public class Friend {
    public String idFriend;
    public String namaFriend;

    public Friend(String idFriend, String namaFriend) {
        this.idFriend = idFriend;
        this.namaFriend = namaFriend;
    }

    public String getIdFriend() {

        return idFriend;
    }

    public void setIdFriend(String idFriend) {
        this.idFriend = idFriend;
    }

    public String getNamaFriend() {
        return namaFriend;
    }

    public void setNamaFriend(String namaFriend) {
        this.namaFriend = namaFriend;
    }
}
